#include <unistd.h>

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	l;

	l = 0;
	if (n == 0)
		return (0);
	while (l < n)
	{
		if (*s1 != *s2 || *s1 == '\0' || *s2 == '\0' )
		{
			return (*s1 - *s2);
		}
		s1++;
		s2++;
		l++;
	}
	return (0);
}
